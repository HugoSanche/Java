public class IfElse {
    public static void main(String[] Args){
        int grade=90;
        if (grade>=90){
            System.out.println("You got a A");
        }
        else if (grade>=80){
            System.out.println("You got a B");
        }
        else if (grade>=70){
            System.out.println("You got a C");
        }
        else if (grade>=60){
            System.out.println("You got a D");            
        }
        else{
            System.out.println("You have to study hard");
        }
    }
}
